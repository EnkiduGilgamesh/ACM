#include <iostream>
#include <string>

using namespace std;

int main(){
    string s;
    cin >> s;

    int len = s.length(), i, ifu;

    for(i = 0; i < len; i++){
        if(s[i] == 'u') ifu = 1;
        if(s[i] == 'd' && ifu == 1){
            s[i] = 's';
            break;
        }
    }

    cout << s << endl;
}