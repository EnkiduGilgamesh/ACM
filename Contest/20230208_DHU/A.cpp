#include <iostream>

using namespace std;

int main(){
    int t, n, i, j;

    cin >> t;

    for(i = 0; i < t; i++){
        cin >> n;

        for(j = 0; j < n; j++){
            cout << (j + n - 1) % n + 1 << " ";
        }

        cout << endl;
    }

    return 0;

}