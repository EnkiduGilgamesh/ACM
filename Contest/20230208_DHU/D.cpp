#include <iostream>

using namespace std;

int main(){
    int t, n, a, i, j, min, max;

    cin >> t;

    // cout << min << endl;

    for(i = 0; i < t; i++){
        cin >> n;

        min = 1e9 + 100, max = 0;

        for(j = 0; j < n; j++){
            cin >> a;

            if(min > a) min = a;
            if(max < a) max = a;
        }

        cout << max - min << endl;
    }

    return 0;
}