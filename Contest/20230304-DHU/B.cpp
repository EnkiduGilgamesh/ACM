#include <iostream>

using namespace std;

long long n, a;

long long solve(long long n){
    if(n > 2){
        return (solve(n / 2) % (a + 2) * (solve(n - n / 2) % (a + 2))) % (a + 2);
    }
    else if(n == 2){
        return 4 % (a + 2);
    }
    else return a;
}

int main(){
    cin >> n;
    a = n;
    cout << solve(n) << endl;
}