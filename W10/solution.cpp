#include <iostream>

using namespace std;

int main(){
    int t, l1, r1, l2, r2, i;
    cin >> t;
    
    int (*res)[2] = new int[t][2];
    for(i = 0; i < t; i++){
        cin >> l1 >> r1 >> l2 >> r2;
        if(l1 <= l2){
            res[i][0] = l1;
            res[i][1] = r2;
        }
        else{
            res[i][0] = r1;
            res[i][1] = l2;
        }
    }

    for(i = 0; i < t; i++){
        cout << res[i][0] << " " << res[i][1] << endl;
    }

    return 0;
}