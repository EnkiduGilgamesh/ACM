#include <iostream>

using namespace std;

// ERROR: tle
int main(){
    int n, i, j, * a, res = 0;
    cin >> n;
    a = new int[n];
    for(i = 0; i < n; i++){
        cin >> a[i];
    }

    for(i = 0; i < n; i++){
        for(j = i + 1; j < n; j++){
            if((a[j] - a[i]) % 200) continue;
            else res++;
        }
    }

    cout << res << endl;
    
    return 0;
}